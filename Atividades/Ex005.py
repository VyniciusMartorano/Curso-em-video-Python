x=input('digite algo:')
print('o tipo primitivo dessa palavra é',type(x))
print('so tem espaços?', x.isspace())
print('é um numero?',x.isnumeric())
print('é alfabetico?',x.isalpha())
print('é alphanumerico?',x.isalnum())
print('esta em maiusculas?',x.isupper())
print('esta em minusculas?{}'.format(x.islower()))
print('é capitalizada {}'.format(x.istitle()))






